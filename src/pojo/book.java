package pojo;

public class book {
	private int bk_id;
	private int bk_borrowed;
	private String bk_name;
	private String bk_img;
	private String bk_author;
	private String bk_category;
	private String bk_intro;
	public int getBk_id() {
		return bk_id;
	}
	public void setBk_id(int bk_id) {
		this.bk_id = bk_id;
	}
	public int getBk_borrowed() {
		return bk_borrowed;
	}
	public void setBk_borrowed(int bk_borrowed) {
		this.bk_borrowed = bk_borrowed;
	}
	public String getBk_name() {
		return bk_name;
	}
	public void setBk_name(String bk_name) {
		this.bk_name = bk_name;
	}
	public String getBk_img() {
		return bk_img;
	}
	public void setBk_img(String bk_img) {
		this.bk_img = bk_img;
	}
	public String getBk_author() {
		return bk_author;
	}
	public void setBk_author(String bk_author) {
		this.bk_author = bk_author;
	}
	public String getBk_category() {
		return bk_category;
	}
	public void setBk_category(String bk_category) {
		this.bk_category = bk_category;
	}
	public String getBk_intro() {
		return bk_intro;
	}
	public void setBk_intro(String bk_intro) {
		this.bk_intro = bk_intro;
	}
	
}
